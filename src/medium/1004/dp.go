func max(a, b int) int {
	if a > b {
		return a
	}
	return b
}

func dfs(nums []int, index int, s int, k int, memo map[K]int) int {
	if index == len(nums) {
		return s
	}
	if val, ok := memo[K{index, s, k}]; ok {
		return val
	}

	size := s
	if nums[index] == 1 {
		size = max(size, dfs(nums, index+1, s+1, k, memo))
	} else {
		// nums[index] == 0, we check if we can flip it to 1
		if k > 0 {
			// we can flip
			size = max(size, dfs(nums, index+1, s+1, k-1, memo))
		}
		size = max(size, dfs(nums, index+1, 0, k, memo))
	}

	memo[K{index, s, k}] = size
	return size

}

type K struct {
	index int
	k     int
	s     int
}

func longestOnes(nums []int, k int) int {
	memo := map[K]int{}
	// start from 0
	return dfs(nums, 0, 0, k, memo)

}
